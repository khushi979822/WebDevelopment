let i = 5;

while (i >= 1) {
    console.log(i);
    i--;   
}

// In this code, the loop variable i is initialized to 5 and the loop continues as long as i is greater than or equal to 1. Inside the loop, the current value of i is printed to the console, and then i is decremented by 1. This will result in the numbers 5 through 1 being printed to the console, and then the loop will terminate when i becomes 0. 