let i = 1;   

while (i <= 5) {  
    console.log(i);   
    i++; 
}

// In this code, the loop variable i is initialized to 1 and the loop continues as long as i is less than or equal to 5. Inside the loop, the current value of i is printed to the console, and then i is incremented by 1. This will result in the numbers 1 through 5 being printed to the console, and then the loop will terminate when i becomes 6.
          